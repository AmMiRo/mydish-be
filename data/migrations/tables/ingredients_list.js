module.exports = tbl => {
    tbl.increments('id')
    tbl.integer('recipe_id')
        .references('id')
        .inTable('recipes')
        .onUpdate('CASCADE')
        .onDelete('set null')
        .notNullable()
    tbl.integer('ingredient_id')
        .references('id')
        .inTable('ingredients')
        .onUpdate('CASCADE')
        .onDelete('set null')
        .notNullable()
    tbl.integer('unit_id')
        .references('id')
        .inTable('units')
        .onUpdate('CASCADE')
        .onDelete('set null')
        .notNullable()
    tbl.integer('quantity')
        .notNullable()
}